int	ft_atoi(const char *str)
{
	int	i;
	int	minus;
	int	result;
	int	b;

	i = 0;
	b = 0;
	minus = 1;
	result = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = (str[i] - '0');
		result = (result * 10 + b);
		i++;
	}
	return (result * minus);
}
